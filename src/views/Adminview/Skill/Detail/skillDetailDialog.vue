<script setup lang="ts">
import { computed, ref, onMounted, watch } from 'vue'
import { useSkillStore } from '@/stores/skills'
import { useSubjectStore } from '@/stores/subject'
import type { VForm } from 'vuetify/components'

const props = defineProps<{ visible: boolean; item: any | null }>()
const emit = defineEmits(['close-dialog'])
const skillStore = useSkillStore()
const subjectStore = useSubjectStore()

const skills = computed(() => skillStore.editedSkill)
const subSkills = computed(() => skillStore.skills)
const selectSkill = ref<any | null>(null)
const form = ref<VForm | null>(null)

const techSkillInput = ref<any>(null) // Input for new tech skill

const localVisible = ref(props.visible)
watch(
  () => props.visible,
  (newVal) => {
    localVisible.value = newVal
  }
)

const closeDialog = async () => {
  emit('close-dialog')
}

function saveSkill() {
  let skill = { ...skills.value }
  skill.techSkills = selectSkill.value
  if (skills.value.id != '') {
    skillStore.updateSkill(skill)
    closeDialog()
  } else {
    const payload: { name: string; description: string; subjects: Object[] } = {
      name: skill.name,
      description: skill.description,
      subjects: skill.subjects
    }
    console.log(payload)

    skillStore.addSkill(payload)
    closeDialog()
  }
}

// Add new tech skill to the list
function addTechSkill() {
  if (techSkillInput.value && !skills.value.techSkills.includes(techSkillInput.value)) {
    skills.value.techSkills.push(techSkillInput.value)
    techSkillInput.value = '' // Clear input after adding
    console.log(skills.value.techSkills)
  }
}

// Remove a tech skill from the list
function removeTechSkill(index: number) {
  skills.value.techSkills.splice(index, 1)
}

onMounted(() => {
  subjectStore.fetchAllSubjects()
})
</script>
<template>
  <v-dialog
    v-model="localVisible"
    max-width="1000px"
    persistent
    style="height: 100vh; overflow-y: auto"
  >
    <v-card
      class="elevation-5"
      rounded="lg"
      max-width="1000px"
      width="100%"
      height="100%"
      style="min-width: 200px"
    >
      <v-container style="width: 100%; height: 100%">
        <div style="display: flex; justify-content: flex-end">
          <v-btn
            color="primary"
            variant="plain"
            @click="closeDialog"
            style="height: 40px; width: 40px"
            class="circular-btn"
            icon="mdi-close"
            rounded="lg"
          />
        </div>
        <p style="font-size: 30px">รายละเอียดสกิล</p>

        <v-row>
          <v-col cols="12">
            <v-text-field
              v-model="skills.name"
              :counter="10"
              label="Name"
              hide-details
              required
            ></v-text-field>
          </v-col>
          <v-col cols="12">
            <v-text-field
              v-model="skills.description"
              :counter="10"
              label="Description"
              hide-details
              required
            ></v-text-field>
          </v-col>
          <v-col cols="12">
            <v-combobox
              v-model="skills.level"
              hide-details
              label="Level"
              :items="[1, 2, 3, 4, 5]"
            ></v-combobox>
          </v-col>

          <v-col cols="12"><p>TechSkills</p></v-col>

          <v-col v-for="(techSkill, index) in skills.techSkills" :key="index" cols="4">
            <v-row align="center">
              <v-col>{{ techSkill.name }}</v-col>
              <v-col cols="auto">
                <v-btn icon @click="removeTechSkill(index)">
                  <v-icon>mdi-close</v-icon>
                </v-btn>
              </v-col>
            </v-row>
          </v-col>

          <v-col cols="12">
            <v-combobox
              v-model="techSkillInput"
              hide-details
              label="Level"
              item-title="name"
              :items="subSkills"
            ></v-combobox>
            <v-btn @click="addTechSkill" class="mt-4">Add Tech Skill</v-btn>
          </v-col>
        </v-row>

        <v-row>
          <v-col>
            <v-btn @click="saveSkill">Save</v-btn>
          </v-col>
        </v-row>
      </v-container>
    </v-card>
  </v-dialog>
</template>
